package main

import "fmt"

func main() {
	var a = 10
	var b = 5
	var jumlah = a + b
	fmt.Println(jumlah) // Output: 15

	var pesan = "Halo, "
	var nama = "John"
	var salam = pesan + nama
	fmt.Println(salam) // Output: Halo, John

	var x = 10
	var y = 5
	var hasil = x > y
	fmt.Println(hasil) // Output: true
}
